print("โปรเเกรมหาพื้นที่ 4 เหลี่ยมจัตุรัส")
side = int(input('ใส่ด้าน : '))
c = side * side
print(f'ค่าพื้นที่ของพื้นที่ 4 เหลี่ยมจัตุรัส = {c} ')

print("\nโปรเเกรมหาพื้นที่ 4 เหลี่ยมผืนผ้า")
Width = int(input('ใส่ความกว้าง : '))
Length = int(input('ใส่ความยาว : '))
c1 = Width * Length
print(f'ค่าพื้นที่ของพื้นที่ 4 เหลี่ยมผืนผ้า = {c1} ')

print("\nโปรเเกรมหาพื้นที่วงกลม")
pi = 3.14
r = float(input('ใส่รัศมีของวงกลม : '))
circle = pi * r * r
print(f'ค่าพื้นที่ของพื้นที่วงกลม = {circle} ')